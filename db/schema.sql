VALUES (1, 60, 90, 'Online Aptitude Test')
ON CONFLICT (id) DO NOTHING;


-- ── Test Sections ─────────────────────────────────────────────────────────────
-- Each row defines one section that appears in the test.
-- questions_per_test: how many questions are drawn randomly per participant.
-- label: short display label shown in the admin UI (e.g. "Section A").
-- Deleting a section also deletes all its questions (handled in app layer).

CREATE TABLE IF NOT EXISTS test_sections (
    id                 SERIAL PRIMARY KEY,
    name               VARCHAR(100) NOT NULL UNIQUE,
    label              VARCHAR(50)  NOT NULL DEFAULT '',
    questions_per_test INT          NOT NULL DEFAULT 16,
    sort_order         INT          NOT NULL DEFAULT 0,
    is_active          BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at         TIMESTAMP             DEFAULT CURRENT_TIMESTAMP
);


-- ── Performance Indexes ───────────────────────────────────────────────────────

CREATE INDEX IF NOT EXISTS participant_answers_submission_id_idx
    ON participant_answers(submission_id);

CREATE INDEX IF NOT EXISTS submission_section_scores_submission_id_idx
    ON submission_section_scores(submission_id);
